package fourth_package;

public class demo01_do_while {
    public static void main(String[] args) {
        /*初始化变量
        do{
        循环语句：
        步进表达式
        }while(比较);
        至少循环一次
         */
        int i=0;
        do{
            System.out.println("张房杰是🐖");
        }while(i>0);
    }
}
